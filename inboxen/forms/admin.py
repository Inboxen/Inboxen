from django import forms

from inboxen.models import Domain


class CreateDomainForm(forms.ModelForm):
    class Meta:
        model = Domain
        fields = ["domain", "owner"]


class EditDomainForm(forms.ModelForm):
    class Meta:
        model = Domain
        fields = ["enabled", "owner"]
